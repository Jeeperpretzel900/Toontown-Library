The Starlight Starbright Children's Foundation offers a range of programs to seriously ill kids and teens and their families including: <br>
<ul>
<li> Starbright World - A private Internet-based online peer support community specifically developed for kids and teens, where they can offer empathy to one another through video conferencing, e-mail, bulletin boards and community activities. Published studies show the network is effective in reducing pain, anxiety and isolation. 
<li> Family Room - An online community that provides information and support to help parents care for their seriously ill children. The site features practical tips, daily solutions, and inspiring personal stories from other parents. 
<li> Starlight Sites - Specially constructed kid-friendly oasis where pediatric patients can relax, entertain themselves, or interact with others, away from the tedium, routine and rigors of being in the hospital.  Playrooms and teen lounges serve as havens within hospitals for pediatric patients and their families.
<li> Fun Centers - Mobile high-tech entertainment units featuring flat panel TVs, DVD players, and Nintendo GameCubes that roll up to a child's bedside and provide both fun and distraction for pediatric patients. 
<li> PC Pals - Mobile computer units that provide bedside entertainment, education and filtered Internet access, enabling hospitalized kids to stay connected to their families and the outside world.
<li> Hospital Happenings - Parties & events for pediatric patients that help combat the loneliness, isolation, and anxiety frequently experienced by hospitalized children.
<li> Kids Activity Network - Sponsored outings and events for seriously ill kids and their families
<li> Videos, CD-ROMs, comic books (The X-Men in Life Lessons) and other computer-based interactive and online programs designed to help kids better understand and cope with their medical conditions.
</ul>
