                Here is your first clue, please follow each part,<br>
                Your next thing will be where a Toon buys a kart.<br>
                When it’s your turn and the clerk looks your way,<br>
                Be sure to tell him, "Have a nice day."
              </span>
              <br><br>

              Well, how about that, a little mystery to kick off my summer. I wasted no time and went straight to the speedway and did just as I was told. The clerk slid me a package, with a note on the top which read.<br><br>

              <span class="poem">
                Please don't open this package, it is one of three,<br>
                You will open them all, when you are with me.<br>
                This next clue is easy, you won’t have to think twice,<br>
                Look for a place, where the pond could turn to ice.<br>
                The one to ask, the one to know,<br>
                Is not a Toon, but a man made of snow.                   
              </span>
              <br><br>

              So off to The Brrrgh I went. Feeling a little silly, I thought, how does one talk to a snowman? I found out very quickly after wishing him a nice day. He was very talkative, and had a remarkable sense of humor. He did notice that I stared at his carrot nose more than I should. He politely laughed it off and told me the rabbits and horses were much more concerning. Then he gave me a package, along with a note which said:<br><br>

              <span class="poem">
                It is time to leave the ice and snow,<br>
                You are heading now to where flowers grow.<br>
                By now you have figured out the phrase,<br>
                Go shout it out in the center of the maze.<br>
                Say it out loud and a package will appear,<br>
                Read the final note, your journey's end is near.            
              </span>
              <br><br>

              I heard Daisy laugh as I ran through her maze. I took a deep breath, braced myself and meowed as loud as I could..."Have a nice day!" POOOF! In a puff of smoke and sparkles, a package appeared, with a note that read:<br><br>

              <span class="poem">
                My name is Samantha Spade, I am a P.I., a sleuth,<br>
                My shop is on Elm Street, called Trellis the Truth.<br>
                Bring the packages as you come my way,<br>
                Oh yes, I almost forgot, "Have a nice day."                    
              </span>
              <br><br> 

              When I arrived there she told me to open each box. FINALLY! As I tore them open, she stood back and watched. Gloves?!!? Inside I found not one, not two, but three pairs of gloves! I just don't get it. What does it mean?<br><br>

              Samantha said, "Don't you see?"<br><br>

              <span class="poem">
                Be it garden gloves, or mittens covered in snow,<br>
                Racing gloves for Toons on the go.<br>
                It is the common we share, that makes us the same,<br>
                It ties us together...it's how we win the game.                               </span>
              <br><br>

              She gave me a picture, to hang on my wall, To remind every Toon...All for one, one for all! I left with a smile, saying, "Thanks for the clues," Oh, but wait! What's the reason that Toons don't wear shoes?<br><br> 

              She turned with a smile, and threw me a wink "Ahhh, that's for next time Miss. Ginger Twinklestink!"              
