<b>December 05, 2008 [sv1.0.37.1.test]</b>
<br><br>
<b>Attention Test Toons!</b>
<br><br>
Players have asked for more ways to chat in-game, and we've listened! The Toon Council is proud to announce our latest addition to Toontown: SpeedChat Plus!
<br><br>
Here is what you need to know to help us test this exciting new feature.
<br><br>
<b>What is SpeedChat Plus:</b>
<ul>
<li>SpeedChat Plus is a form of type chat that allows users to communicate by using the Disney SpeedChat Plus dictionary. Words that are not in the dictionary will be highlighted in red. You then have the opportunity to choose alternative words prior to sending your message. 
</ul>
<b>How do I enable SpeedChat Plus:</b>
<ul>
<li>SpeedChat Plus, along with your other chat options, is managed through the <u>Preferences</u> section of Member Services. Click <a href="https://account.test.toontown.com/webChangeChat.php" target="_blank">here</a> to go to the Preferences section now and select SpeedChat Plus. Then use the Parent Password linked with your account to turn this feature on.
</ul>
<b>How to use SpeedChat Plus:</b>
<ul>
<li>Once SpeedChat Plus has been enabled, you can start using SpeedChat Plus in-game by typing words on your keyboard -- or -- by clicking on the blue "Type Chat" button in the upper-left corner of the play screen. Your messages are viewable to all who have SpeedChat Plus enabled within your vicinity. If you are whispering to a specific player, only that player will see your message.
<br><br><b>*** NOTE ***</b> currently if you try to Whisper to a player, the blue "Type Chat" button in the Whisper chat panel is disabled.  This will be fixed in the next release.
</ul>
 

Please take some time to test this new chat feature through all of Toontown. We are still adding functionality, so please send any comments, bug reports, or word suggestions to us through the Contact Us form.
<br><br>
Thank you for your help!
<br><br>
<b>Special note for Open Chat with True Friends players:</b>
<br><br>
Players who have both SpeedChat Plus and Open Chat with True Friends enabled can communicate with other players according to those players' chat preferences. For example:
<ul>
<li>If the player you are chatting with has SpeedChat Plus, they will only see words from the Disney SpeedChat Plus dictionary. Words not in the dictionary will be replaced with animal sounds.
<br><br>
<li>If the player you are chatting with is your True Friend, your can chat openly through Open Chat with True Friends. When chatting openly with multiple True Friends at once, your chat may be viewable to other non-True Friend players who have SpeedChat Plus within your vicinity, provided the words are in the Disney SpeedChat Plus dictionary. Open Chat with True Friends whispers will only be viewable by the True Friend you are whispering to.
<br><br>
<b>*** NOTE ***</b> currently Open Chat with True Friends' Whispers does not work correctly, wherein all typed words are showing up as animal sounds.  This will be fixed in the next release.
</ul>

