      <center><br><img src="/shared/images/firewall.gif" width="431" height="100"></center>
      <center>Please select your firewall software from the list below for instructions on how to configure your firewall to work with Toontown.</center><br>
      <ul>
        <li><a href="#norton">Norton Internet Security</a></li>
        <li><a href="#mcafee">McAfee Personal Firewall and Security Center</a></li>
        <li><a href="#micf1">Microsoft Internet Connection Firewall XP SP1</a></li>
        <li><a href="#micf2">Microsoft Internet Connection Firewall XP SP2</a></li>
        <li><a href="#za">ZoneAlarm</a></li>
        <li><a href="#tpf">Tiny Personal Firewall</a></li>
        <li><a href="#sygate">Sygate Personal Firewall</a></li>
      </ul>
      
      <p><a name="norton"><b>Norton Internet Security</b></a></p>
      <p>
      <ol>
        <li>Double click on the world icon in your taskbar.</li>
        <li>Select <i>Personal Firewall</i></li>
        <li>Click <i>Configure</i></li>
        <li>Select the <i> Programs </i>tab at the top.</li>
        <li>Select the <i>Add</i> button.</li>
        <li>In the <i>Look In field </i>navigate to C:-> Program files -> Disney -> Disney Online -> Toontown</li>
        <li>Select Toontown.exe and click <i>Open</i></li>
        <li>Select <i>Permit</i> from the drop down box and click <i>OK</i>.</li>
        <li>On the next screen select <i>Networked Games</i> and click <i>OK</i>.</li>
        <li>Click <i>OK</i> again to exit out.</li>
      </ol>
      </p>
      <p><a name="mcafee"><b>McAfee Personal Firewall and Security Center</b></a></p>
      <p>
      <ol>
        <li>Double Click on the <i>Red Box with an M </i>in the taskbar.</li>
        <li>Select the <i>Personal Firewall+ </i> Tab</li>
        <li>Click on the <i>View the Internet Applications List</i> icon. A new window will open up.</li>
        <li>Click on the <i>Utilities</i>  Tab.</li>
        <li>Click on the <i>Security Settings</i> Icon.</li>
        <li>Change your current settings to <i>Trusting</i>.</li>
      </ol>
      If you need additional help configuring your McAfee product to work with Toontown, please contact McAfee technical support <a href="http://ts.mcafeehelp.com/default.asp?siteID=1&cnrcheck=done&resolution=800x600">here</a>.

      </p>

      <p><a name="micf1"><b>Microsoft Internet Connection Firewall (XP Service Pack 1)</b></a></p>
      <p>
        <ol>
        <li> Right click on <i>My Network Places</i> and click <i> Properties</i></li>
        <li> Right click on <i>LAN Connection </i> (or the name of your broadband connection) and click <i> Properties</i></li>
        <li> Click the <i>Advanced</i> Tab</li>
        <li> Click the check box that states <i>Protect my computer and network by limiting or preventing access to this computer from the internet.</i></li>
        <li> Click <i>Apply</i> and close window.</li>
      </ol>
      Please note: This is not recommended unless you have another firewall or other protection enabled.
      </p>

      <p><a name="micf2"><b>Microsoft Internet Connection Firewall (XP Service Pack 2)</b></a></p>
      <p>
      <ol>
        <li> Go to Start -> Control Panel.</li>
        <li> Double Click on the <i> Windows Firewall</i>  Icon.</li>
        <li> Select the <i> Exceptions Tab</i>  at the top.</li>
        <li> Click on the <i> Add Program</i>  button.</li>
        <li> Click on the <i> Browse</i>  button</li>
        <li> Navigate to C:\Program Files\Disney\Disney Online\Toontown and select Toontown.exe and select <i> Open</i> .</li>
        <li> Click <i>OK</i>  once. Make sure that Toontown.exe is checked and then click <i>Ok</i>  again.</li>
      </ol>
      </p>

      <p><a name="za"><b>ZoneAlarm</b></a></p>
      <p>
      <ol>
        <li> Double click on the <i>Zone Alarm</i> system tray icon (the system tray icon is located in the lower right hand corner of your screen where your clock is)</li>
        <li> Click on <i>Program Control</i></li>
        <li> Slide the Tab to the <i>LOW</i> setting</li>
        <li> Close the window.</li>
      </ol>
      </p>

      <p><a name="tpf"><b>Tiny Personal Firewall</b></a></p>
      <p>
      <ol>
        <li> Right click on the <i>Tiny Personal Firewall</i> system tray icon and select <i>Administration Tool.</i></li>
        <li> Click <i>Application Groups</i></li>
        <li> Click <i>Settings</i> and then click </i>Create new Application Group</i></li>
        <li> Name the new application group "Disney's Toontown Online" and set the Security Level to <i>Unrestricted</i></li>
        <li> Go into the <i>Unrestricted Applications</i> group and look for all executable files listed under your Disney's Toontown Online directory and move them to the Disney's Toontown Online Application Group you just created</li>
        <li> Close the window.</li>
      </ol>
      </p>

      <p><a name="sygate"><b>Sygate Personal Firewall</b></a></p>
      <p>
      <ol>
        <li>Right click on the <i>Sygate Personal Firewall</i> icon in the system tray and left click <i>Sygate Personal Firewall Pro</i>.</li>
        <li>Click <i>Security</i> and then click <i>Allow All</i>.</li>
        <li>Close the window.</li>
      </ol>
      </p>
